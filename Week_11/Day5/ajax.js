//What’s AJAX?

//AJAX stands for Asynchronous JavaScript And XML.

//It’s the use of the XMLHttpRequest object to communicate with servers. 
// It can send and receive information in various formats, including JSON, XML, HTML, and text files.

//AJAX’s most appealing characteristic is its “asynchronous” nature, 
// which means it can communicate with the server, exchange data, and update the page without 
// having to refresh the page.

//The two significant features of AJAX allow you to do the following:

//    Make requests to the server without reloading the page
//    Receive and work with data from the server


